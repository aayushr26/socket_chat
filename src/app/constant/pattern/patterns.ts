export const PATTERN = {
    email:/^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
    //  password:  /^[^\s]+$/,
    password:  /^(?=(.*[a-z]){1,})(?=(.*[\d]){1,})(?=(.*[\W]){1,})(?!.*\s).{8,20}$/,
    name:/(.|\s)*\S(.|\s)*/,
    phone: "^[0-9]+$",
    price:/(^[0][1-9]+)|([1-9]\d*)+$/
};